
package Modelo;


public class Repuesto {
     int rep_id;
     String rep_nombre;
     String rep_precio;
     String rep_descripcion;
     String rep_fecha_importacion;


    public int getRep_id() {
        return rep_id;
    }
public void setRep_id(int rep_id) {
        this.rep_id=rep_id;
    }
    public String getRep_nombre() {
        return rep_nombre;
    }

    public void setRep_nombre(String rep_nombre) {
        this.rep_nombre = rep_nombre;
    }

    public String getRep_precio() {
        return rep_precio;
    }

    public void setRep_precio(String rep_precio) {
        this.rep_precio = rep_precio;
    }

    public String getRep_descripcion() {
        return rep_descripcion;
    }

    public void setRep_descripcion(String rep_descripcion) {
        this.rep_descripcion = rep_descripcion;
    }

    public String getRep_fecha_importacion() {
        return rep_fecha_importacion;
    }
    public void setRep_fecha_importacion(String rep_fecha_descripcion) {
        this.rep_fecha_importacion=rep_fecha_descripcion;
    }

  
    }

    

    

